const normalize = (str) => {
    if (!str) return "";
    const map = {
        'a': 'а', 'c': 'с', 'e': 'е', 'o': 'о', 'p': 'р', 'x': 'х', 'y': 'у',
        'A': 'А', 'B': 'В', 'E': 'Е', 'K': 'К', 'M': 'М', 'H': 'Н', 'O': 'О', 'P': 'Р', 'C': 'С', 'X': 'Х', 'T': 'Т'
    };
    return str.split('').map(char => map[char] || char).join('').toLowerCase();
};

try {
    const data = JSON.parse(fs.readFileSync(path, 'utf8'));
    let totalFixed = 0;

    data.forEach(variant => {
        variant.questions.forEach(q => {
            const qNormalized = normalize(q.text);
            
            officialRules.forEach(rule => {
                const ruleQNormalized = normalize(rule.q);
                if (qNormalized.includes(ruleQNormalized)) {
                    // Find the best match: prefer exact inclusion of the full string, then fuzzy
                    const matches = q.options.filter(o => normalize(o.text).includes(normalize(rule.a)));
                    
                    let correctOption = null;
                    if (matches.length > 1) {
                        // If multiple matches (e.g. "Оклад" and "Оклад + выплаты"), pick the one closest in length to the rule or the longest one
                        correctOption = matches.sort((a, b) => b.text.length - a.text.length)[0];
                    } else if (matches.length === 1) {
                        correctOption = matches[0];
                    }

                    if (correctOption) {
                        if (q.correctAnswer !== correctOption.id) {
                            console.log(`Fixing [${variant.title}]: Q: "${q.text.substring(0, 40)}..." -> ${correctOption.id} (${correctOption.text.substring(0, 30)}...)`);
                            q.correctAnswer = correctOption.id;
                            totalFixed++;
                        }
                    }
                }
            });
        });
    });

    fs.writeFileSync(path, JSON.stringify(data, null, 2));
    console.log(`OFFICIAL SYNC COMPLETE: Corrected ${totalFixed} answers using user's official key.`);
} catch (e) {
    console.error('Sync Error:', e);
}
